class Switch {

    public static void main(String[] args) {
        int key = 1;
        switch (key) {
            case 1:
                System.out.println("Im at one");
                // break;

            case 2:
                System.out.println("Im at two");
                break;

            case 3:
                System.out.println("Im at three");
                break;
 
            default:
                break;
        }
    }
}
